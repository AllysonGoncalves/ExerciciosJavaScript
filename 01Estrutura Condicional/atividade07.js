/*EXERCICIO - LOGICA DE PROGRAMAÇÃO - ESTRUTURA CONDICIONAL
Ex: 07. 

Faça programa que leia uma temperatura em graus Celsius, calcular e escrever o valor
correspondente em graus Fahrenheit.. 
*/

// Solicita ao usuário a temperatura em graus Celsius
let celsius = parseFloat(prompt("Digite a temperatura em graus Celsius:"))

// Calcula a temperatura em Fahrenheit
let fahrenheit = (celsius * 9/5) + 32

// Exibe o resultado
alertconsole.log("A temperatura em Fahrenheit é: " + fahrenheit.toFixed(2))
console.log("A temperatura em Fahrenheit é: " + fahrenheit.toFixed(2))